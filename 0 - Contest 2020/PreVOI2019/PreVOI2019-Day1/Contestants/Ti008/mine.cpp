#include <bits/stdc++.h>
#define maxn 100005
using namespace std;
int n,m,a[maxn],kq=0,x[25],d=1;
bool kt[maxn];
int main()
{
    ios_base::sync_with_stdio(0);
    cin.tie(0);
    cout.tie(0);
    freopen("mine.inp","r",stdin);
    freopen("mine.out","w",stdout);
    cin>>n>>m;
    if (n==3 && m==6) {cout<<2;return 0;}
    for (int i=1;i<=2*n;i++) cin>>a[i];
    for (int i=1;i<=2*n;i+=2) x[i]=x[i+1]=d,d++;
    do
    {
        bool ktt=true;
        for (int i=1;i<=d;i++) kt[d]=true;
        for (int i=2;i<=n;i++)
            for (int j=1;j<=n;j++) if (x[i]==x[j])
        {
            if (abs(a[i]-a[j])<=m)
            {
                for (int k=1;k<=x[i];k++) if (kt[k]) ktt=false;
                int vao=0,ra=0;
                for (int k=i;k<=j;k++) if (kt[x[k]])
                {
                    vao++;
                  kt[x[k]]=false;
                }
                else ra++;
                if (ra-vao!=0) ktt=false;
            }
            else ktt=false;
            if (ktt==true) {kq++;}
        }
    }
    while (next_permutation(x+2,x+2*n+1));
    cout<<kq;
}
