#include <bits/stdc++.h>
#define maxn 100005
using namespace std;
int a[18]={1,3,5,10,30,50,100,300,500,1000,3000,5000,10000,30000,50000}
,q,f[maxn];
int main()
{
    ios_base::sync_with_stdio(0);
    cin.tie(0);
    cout.tie(0);
    freopen("oddcoin.inp","r",stdin);
    freopen("oddcoin.out","w",stdout);
    cin>>q;

    while (q--)
    {
        int x,t;
        cin>>x>>t;
        for (int i=1;i<=maxn-5;i++) f[i]=1e9;
        a[15]=x;
    for (int i=0;i<=15;i++) f[a[i]]=1;
    for (int j=2;j<=maxn-5;j++)
        for (int i=0;i<=15;i++)
            if (j-a[i]>0) f[j]=min(f[j],f[j-a[i]]+1);
    cout<<f[t]<<endl;
    }
}
